					<h1>Масштабні автомоделі (машини) в масштабі 1:43</h1>
					<p><img src="images/212_1.jpg" border="0" alt="моделі автомобілів 1:43" title="моделі автомобілів 1:43" width="240" height="135" style="float: right; margin: 5px;">Наш інтернет-магазин колекційних масштабних моделей автомобілів пропонує на Ваш вибір велику колекцію моделей машин різних вітчизняних і зарубіжних виробників. Ми намагаємося запропонувати Вам в самому широкому асортименті такі моделі як: легкові, вантажні та службові автомобілі, спорткари, автобуси, мотоцикли, спецтехніка, військова техніка та інші.</p>
					<h2>Масштабні моделі 1:43</h2>
					<p><img src="images/121_1.jpg" border="0" alt="моделі автомобілів 1:43" title="моделі автомобілів 1:43" width="240" height="135" style="float: right; margin: 5px;">Важко собі уявити сучасне життя без автомобілів. При цьому все більше число людей проявляють інтерес не тільки до останніх новинок автомобільного ринку, але і до безпосередньо історії автомобілебудування, в тому числі і нашої країни. Що стосується появи автомобіля, то дякувати за це треба, перш за все, німецьких конструкторів Готтлиба Даймлера і Карда Бенца. В інших країнах епоху автомобілізації пов'язують з наступними іменами: у Франції Луї Пенар і Еміль Льовассор, в Англії Фредерік Ланчестер і Джон Найт, в США Френк Дюріе і Генрі Форд, в Італії Енріке Бернарди і Арістід Фачьоллі, в Росії Євген Яковлєв та Петро Фрезе. Історія автомобіля дуже велика і цікава тема, яка подарує багато позитивних емоцій при її вивченні. Саме тому автомобільні масштабні моделі в Україні так популярні. Варто зазначити, що особливою популярністю користуються колекційні моделі автомобілів в співвідношенні 1:43. Незважаючи на те, що існують і моделі з іншим співвідношенням, саме моделі автомобілів 1:43 мають найбільший попит. Це своєрідний еталон серед колекційних моделей, саме тому автомоделей 1:43 в Україні користуються найбільшою популярністю серед колекціонерів.</p>
					<h2>Моделі 1:43: переваги перед іншими масштабами</h2>
					<p><img src="images/205_1.jpg" border="0" alt="масштабні моделі 1:43 Україна" title="масштабні моделі 1:43 Україна" width="240" height="135" style="float: left; margin-left: 5px; margin-right: 5px;">Головною перевагою, яким володіють масштабні моделі 1:43 є невеликий розмір. Завдяки компактним габаритам модель не займе багато місця. Автомоделей 1:43 виготовляються зазвичай з металу або пластику, і мають високу ступінь опрацювання деталей. Якщо більш детально придивитися до масштабних моделей 1:43, то стає зрозуміло за рахунок чого вони стали такими популярними. Моделі в масштабі 1:43 займають менше місця на полиці, ніж, наприклад, моделі в масштабі 1:24. Крім того, в таких моделях максимальна деталізація: салон, скляні фари, колеса і т.д. У багатьох моделей можуть відкриватися капот, двері, багажник. Виробництвом масштабних моделей займається невелика кількість компаній, тому вони настільки популярні серед колекціонерів. Колекційні моделі 1:43 (Україна) представлені в дуже великій різноманітності: моделі цивільних автомобілів СРСР і зарубіжжя, військова техніка, громадський транспорт і багато іншого.</p>
